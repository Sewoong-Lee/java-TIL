package java01;

//파일 이름과 public class의 이름은 같아야 한다.(빈 안의 이름도 동일해야함)
public class J20210412_01_main {  //클래스의 이름  
	
	// main 메소드 : 진입점 메소드 (무조건 1개)
	public static void main(String[] args) { //()안에 있는건 매개변수 

		System.out.print("자아바");  //뒤에 ln을 지우면 줄바꿈 안댐
		System.out.println("나의 나이는 " + 20);
		System.out.println(10+ "+" +20+ "=" +(10+20));
		
		System.out.printf("%d + %d = %d\n", 10,20,10+20); //포맷문자
		
		
		
		
	}

}
